public class Karyawan {
    int gaji;
    String nama;
    public void setGaji(int gaji) {
        this.gaji = gaji;
    }
    public void setNama(String nama) {
        this.nama = nama;
    }
    public int getGaji() {
        return gaji;
    }
    public String getNama() {
        return nama;
    }
}
